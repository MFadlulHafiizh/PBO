    private void loadData(){
        try{
            record = brgServis.getAll();
        }catch(Exception e){
            Logger.getLogger(barangView.class).log(Level.SEVERE, null, e);
        }
    }
    
    private void isiTabel(){
        Object data[][] = new Object[record.size()][4];
        int x = 0;
        for(Barang barang : record){
            data[x][0] = barang.getKodeBarang();
            data[x][1] = barang.getNamaBarang();
            data[x][2] = barang.getJumlahBarang();
            data[x][3] = barang.getHargaBarang();
            x++;
        }
        String [] judul = {"kode barang", "nama barang", "Jumlah", "Harga"};
        tbl_barang.setModel(new DefaultTableModel(data, judul));
        jScrollPane1.setViewportView(tbl_barang);
    }
    
    private void isiText(){
        Barang barang = record.get(row);
        txt_kode.setText(barang.getKodeBarang());
        txt_nama.setText(barang.getNamaBarang());
        txt_jumlah.setText(String.valueOf(barang.getJumlahBarang()));
        txt_harga.setText(String.valueOf(barang.getHargaBarang()));
    }
    
    private void kosongkanText(){
        txt_kode.setText("");
        txt_nama.setText("");
        txt_jumlah.setText("");
        txt_harga.setText("");
    }
    private void statusAwal(){
        kosongkanText();
        loadData();
        isiTabel();
      }
