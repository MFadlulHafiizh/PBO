    private void txt_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_tambahActionPerformed
        try{
            Barang barang = new Barang();
            barang.setKodeBarang(txt_kode.getText());
            barang.setNamaBarang(txt_nama.getText());
            barang.setJumlahBarang(Integer.valueOf(txt_jumlah.getText()));
            barang.setHargaBarang(Integer.valueOf(txt_harga.getText()));
            
            brgServis.insert(barang);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "Data Tersimpan");
        } catch(Exception ex){
            Logger.getLogger(barangView.class).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }   
        statusAwal();
    }//GEN-LAST:event_txt_tambahActionPerformed

    private void txt_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_editActionPerformed
           try{
            Barang barang = new Barang();
            barang.setKodeBarang(txt_kode.getText());
            barang.setNamaBarang(txt_nama.getText());
            barang.setJumlahBarang(Integer.valueOf(txt_jumlah.getText()));
            barang.setHargaBarang(Integer.valueOf(txt_harga.getText()));
            
            brgServis.update(txt_kode.getText(), barang);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "Data Tersimpan");
        } catch(Exception ex){
            Logger.getLogger(barangView.class).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        } 
    }//GEN-LAST:event_txt_editActionPerformed

