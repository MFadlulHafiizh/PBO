    private void loadData(){
        try {
            record = plgServis.getAll();
        } catch (SQLException ex) {
            Logger.getLogger(pelangganView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void isiTabel(){
        Object data[][]=new Object[record.size()][5];
        int x = 0;
        for (Pelanggan plg : record) {
            data[x][0]=plg.getId_pelanggan();
            data[x][1]=plg.getNama();
            data[x][2]=plg.getJeniskel();
            data[x][3]=plg.getAlamat();
            data[x][4]=plg.getNotlp();
            x++;
        }
        String [] judul={"id pelanggan","nama","jenis kelamin","alamat","notlp"};
        tabelPelanggan.setModel(new DefaultTableModel(data, judul));
        jScrollPane3.setViewportView(tabelPelanggan);
    }
    
    private void isiText(){
        Pelanggan plg=record.get(row);
        txtIdPelanggan.setText(plg.getId_pelanggan());
        txtNama.setText(plg.getNama());
        if(plg.getJeniskel().equals("Laki-laki")){
            ckLaki.setSelected(true);
            ckCewe.setSelected(false);
        }else{
            ckCewe.setSelected(true);
            ckLaki.setSelected(false);
        }
        txtalamat.setText(plg.getAlamat());
        txtnotlp.setText(plg.getNotlp());
    }
    
    void kosongkanText(){
        txtIdPelanggan.setText("");
        txtNama.setText("");
        txtalamat.setText("");
        txtnotlp.setText("");
        ckLaki.setSelected(false);
        ckCewe.setSelected(false);
    }
    
    private void statusAwal(){
        kosongkanText();
        loadData();
        isiTabel();
    }

