    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try{
            Pelanggan pelanggan = new Pelanggan();
            pelanggan.setId_pelanggan(txtIdPelanggan.getText());
            pelanggan.setNama(txtNama.getText());
            if(ckLaki.isSelected()){
                pelanggan.setJeniskel("Laki-laki");
            }else if(ckCewe.isSelected()){
                pelanggan.setJeniskel("Perempuan");
            }
            pelanggan.setAlamat(txtalamat.getText());
            pelanggan.setNotlp(txtnotlp.getText());
            plgServis.insert(pelanggan);
            this.statusAwal();
            JOptionPane.showMessageDialog(this, "Data tersimpan");
        }catch(SQLException ex){
            Logger.getLogger(pelangganView.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

